# Operadores Aritmeticos

num1 = 10
num2 = 3

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2
resto_divisao = num1 % num2
potencia = num1 ** num2

print(f"Soma: {soma}, Subtração: {subtracao}")
print(f"Multiplicação: {multiplicacao}, Divisão: {divisao}")
print(f"Resto da Divisão: {resto_divisao}, Potência: {potencia}")

# Operadores de Comparação (Relacionais)
x = 5
y = 8

igual = x == y
diferente = x != y
menor_que = x < y
maior_que = x > y
menor_ou_igual = x <= y
maior_ou_igual = x >= y

print(f"Igual: {igual}, Diferente: {diferente}")
print(f"Menor que: {menor_que}, Maior que {maior_que}")
print(f"Menor ou Igual: {menor_ou_igual}, Maior ou Igual: {maior_ou_igual}")

# Operadores Lógicos

var1 = True
var2 = False

resultado_and =  var1 and var2
resultado_or = var1 or var2
resultado_not = not var1

print(f"AND: {resultado_and}")
print(f"OR: {resultado_or}")
print(f"NOT: {resultado_not}")
